package org.jumpmind.symmetric.io.data.transform;

public class TransformColumnException extends RuntimeException {

    private static final long serialVersionUID = 1L;

    public TransformColumnException() {
        super();
    }

    public TransformColumnException(String message, Throwable cause) {
        super(message, cause);
    }

    public TransformColumnException(String message) {
        super(message);
    }

    public TransformColumnException(Throwable cause) {
        super(cause);
    }

}
